package Lecture11;

public class Array_2D {

	public static void main(String[] args) {
		int [][] mat = new int [5][3]; // syntax of 2d array
		                 // [Row][Column] 
		System.out.println(mat); // this will print the address of the big array
		System.out.println(mat.length); // this will print the size of the big array
		System.out.println(mat[0]); //this will print the address of the first row 
		System.out.println(mat[0].length); // this will print the size of the first row
		
	}

}
